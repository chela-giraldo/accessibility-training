#!/usr/bin/env node
// Fetches quiz data from a public Google Sheet (CSV export) and writes src/quizzes.js
// Run: npm run sync
// Sheet format: module_id | question | option_a | option_b | option_c | option_d | answer_index | explanation

import { writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";

const SHEET_URL = process.env.SHEET_URL;

if (!SHEET_URL) {
  console.error(
    "\n❌  SHEET_URL is not set.\n\n" +
    "1. Open your Google Sheet\n" +
    "2. File → Share → Publish to web → Sheet1 → CSV → Publish\n" +
    "3. Copy the URL and run:\n\n" +
    "   SHEET_URL='<your-csv-url>' npm run sync\n\n" +
    "Or add it to a .env.local file as:\n" +
    "   SHEET_URL=<your-csv-url>\n"
  );
  process.exit(1);
}

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = join(__dirname, "../src/quizzes.js");

console.log("⬇️  Fetching sheet…");
const res = await fetch(SHEET_URL);
if (!res.ok) throw new Error(`HTTP ${res.status} — check your SHEET_URL`);
const csv = await res.text();

const lines = csv.trim().split("\n").slice(1); // skip header row
const byModule = {};

for (const raw of lines) {
  // Parse CSV row — handles quoted fields with commas inside
  const cols = parseCSVRow(raw);
  if (cols.length < 8) continue;

  const [module_id, question, a, b, c, d, answer_index, explanation] = cols.map(s => s.trim());
  const id = parseInt(module_id, 10);
  if (isNaN(id) || !question) continue;

  const options = [a, b, c, d].filter(Boolean);
  const answer = parseInt(answer_index, 10);

  if (!byModule[id]) byModule[id] = [];
  byModule[id].push({ question, options, answer, explanation });
}

// Build the exported object
const entries = Object.entries(byModule)
  .sort(([a], [b]) => Number(a) - Number(b))
  .map(([id, qs]) => {
    const inner = qs.map(q => {
      const opts = q.options.map(o => JSON.stringify(o)).join(", ");
      return (
        `    { question: ${JSON.stringify(q.question)}, ` +
        `options: [${opts}], ` +
        `answer: ${q.answer}, ` +
        `explanation: ${JSON.stringify(q.explanation)} }`
      );
    }).join(",\n");
    return `  ${id}: [\n${inner}\n  ]`;
  })
  .join(",\n");

const output =
  `// AUTO-GENERATED by scripts/sync-from-sheets.js — do not edit manually\n` +
  `// Edit the Google Sheet and run: npm run sync\n\n` +
  `export const QUIZZES = {\n${entries}\n};\n`;

writeFileSync(OUT, output, "utf8");
console.log(`✅  Wrote src/quizzes.js (${Object.keys(byModule).length} modules, ${lines.length} questions)`);

// Minimal CSV parser that handles quoted fields
function parseCSVRow(row) {
  const result = [];
  let cur = "";
  let inQuotes = false;
  for (let i = 0; i < row.length; i++) {
    const ch = row[i];
    if (ch === '"') {
      if (inQuotes && row[i + 1] === '"') { cur += '"'; i++; }
      else inQuotes = !inQuotes;
    } else if (ch === "," && !inQuotes) {
      result.push(cur); cur = "";
    } else {
      cur += ch;
    }
  }
  result.push(cur);
  return result;
}
